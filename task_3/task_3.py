import sys
import os
from pathlib import Path
from colorama import Fore, Style
from typing import List

COLOR_DIR = Fore.BLUE + Style.BRIGHT
COLOR_FILE = Fore.GREEN              
COLOR_RESET = Style.RESET_ALL

ELBOW = "‚îî‚îÄ‚îÄ"
TEE = "‚îú‚îÄ‚îÄ"
PIPE = "‚îÇ  "
SPACE = "   "

def display_tree(root_path=Path, prefix='', is_root=True):
    if is_root:
        root_name = root_path.name if root_path.name else root_path.resolve().name
        print(f"{COLOR_DIR}# üì¶{root_name}{COLOR_RESET}")

    try:
        entries = sorted(list(root_path.iterdir()))
    except Exception as e:
        print(f"{prefix} ‚ùå {Fore.RED}–ü–æ–º–∏–ª–∫–∞ –¥–æ—Å—Ç—É–ø—É –¥–æ {root_path.name}: {e}{COLOR_RESET}")
        return

    items_count = len(entries)

    for i, entry in enumerate(entries):
        is_last = (i == items_count - 1)

        connector = ELBOW if is_last else TEE

        if entry.is_dir():
            color_line = f"{COLOR_DIR}{connector} üìÇ{entry.name}{COLOR_RESET}"
            print(prefix + color_line)

            next_prefix = prefix + (SPACE if is_last else PIPE)

            display_tree(entry, next_prefix, is_root=False)
        else:
            color_line = f"{COLOR_FILE}{connector} üìú{entry.name}{COLOR_RESET}"
            print(prefix + color_line)

def visualize_directory_structure():
    if len(sys.argv) < 2:
        print(f"{Fore.RED}–ü–æ–º–∏–ª–∫–∞: –ù–µ–æ–±—Ö—ñ–¥–Ω–æ –≤–∫–∞–∑–∞—Ç–∏ —à–ª—è—Ö –¥–æ –¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ—ó —è–∫ –∞—Ä–≥—É–º–µ–Ω—Ç.")
        print(f"–í–∏–∫–æ—Ä–∏—Å—Ç–∞–Ω–Ω—è: python3 {os.path.basename(sys.argv[0])} <—à–ª—è—Ö/–¥–æ/–¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ—ó>{COLOR_RESET}")
        sys.exit(1)

    path_arg = sys.argv[1]
    target_path = Path(path_arg)

    if not target_path.exists():
        print(f"{Fore.RED}–ü–æ–º–∏–ª–∫–∞: –®–ª—è—Ö '{path_arg}' –Ω–µ —ñ—Å–Ω—É—î.{COLOR_RESET}")
        sys.exit(1)

    if not target_path.is_dir():
        print(f"{Fore.RED}–ü–æ–º–∏–ª–∫–∞: –®–ª—è—Ö '{path_arg}' –Ω–µ —î –¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ—î—é.{COLOR_RESET}")
        sys.exit(1)

    print("--- –°—Ç—Ä—É–∫—Ç—É—Ä–∞ –¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ—ó ---")
    display_tree(target_path)
    print("----------------------------")


visualize_directory_structure()
